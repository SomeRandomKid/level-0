
public class ForLoopGauntlet {
	public static void main(String [] args){
	for (int i = 0; i <= 100; i++) {	
		System.out.println(i);
		}
	for (int j = 100; j >= 0; j--) {
		System.out.println(j);
		}
	for (int k = 2; k <= 100; k += 2) {
		System.out.println(k);
		}
	for (int l = 1; l <= 99; l += 2) {
		System.out.println(l);
		}
	}
}
